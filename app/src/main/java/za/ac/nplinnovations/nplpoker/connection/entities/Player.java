package za.ac.nplinnovations.nplpoker.connection.entities;

public class Player {
    private String cards;
    private String hand;
    private String result;

    public Player() {
    }

    public String getCards() {
        return cards;
    }

    public String getHand() {
        return hand;
    }

    public String getResult() {
        return result;
    }
}
